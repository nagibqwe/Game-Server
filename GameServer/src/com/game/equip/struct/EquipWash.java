package com.game.equip.struct;

public class EquipWash {

    /**
     * 条目ID
     */
    private int id;

    /**
     * 条目百分比
     */
    private int per;

    /**
     * 条目祝福值
     */
    private int value;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPer() {
        return per;
    }

    public void setPer(int per) {
        this.per = per;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
